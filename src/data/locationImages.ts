// Fallback: colored placeholder with city name
export function getPlaceholderImage(cityName: string, seed?: number): string {
  const colors = ['1a1a2e', '16213e', '0f3460', 'e94560', '533483'];
  const color = colors[(seed || cityName.length) % colors.length];
  const text = encodeURIComponent(cityName);
  return `https://placehold.co/800x600/${color}/white?text=${text}`;
}

// Get image for a city - fallback chain
export function getCityImage(city: string): string {
  // Direct match
  if (LOCATION_IMAGES[city]) {
    return LOCATION_IMAGES[city];
  }
  
  // Try partial match (e.g., "Rio de Janeiro" vs "Rio")
  const cityLower = city.toLowerCase();
  for (const [key, url] of Object.entries(LOCATION_IMAGES)) {
    if (key.toLowerCase().includes(cityLower) || cityLower.includes(key.toLowerCase())) {
      return url;
    }
  }
  
  // Fallback to placeholder
  return getPlaceholderImage(city);
}
