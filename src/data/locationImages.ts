// Fallback: colored placeholder with city name
export function getPlaceholderImage(cityName: string, seed?: number): string {
  const colors = ['1a1a2e', '16213e', '0f3460', 'e94560', '533483'];
  const color = colors[(seed || cityName.length) % colors.length];
  const text = encodeURIComponent(cityName);
  return `https://placehold.co/800x600/${color}/white?text=${text}`;
}

// Normalize city name for matching (handles umlauts)
function normalizeForMatch(name: string): string {
  return name
    .toLowerCase()
    .trim()
    .replace(/ä/g, 'ae')
    .replace(/ö/g, 'oe')
    .replace(/ü/g, 'ue')
    .replace(/ß/g, 'ss')
    .replace(/é/g, 'e')
    .replace(/è/g, 'e')
    .replace(/ê/g, 'e')
    .replace(/á/g, 'a')
    .replace(/à/g, 'a')
    .replace(/ñ/g, 'n')
    .replace(/ó/g, 'o')
    .replace(/ú/g, 'u')
    .replace(/ç/g, 'c')
    .replace(/ş/g, 's')
    .replace(/ğ/g, 'g')
    .replace(/ı/g, 'i');
}

// Get image for a city - fallback chain
export function getCityImage(city: string): string {
  // Direct match
  if (LOCATION_IMAGES[city]) {
    return LOCATION_IMAGES[city];
  }
  
  // Normalized match (handles umlauts: Zürich vs Zuerich)
  const cityNorm = normalizeForMatch(city);
  for (const [key, url] of Object.entries(LOCATION_IMAGES)) {
    if (normalizeForMatch(key) === cityNorm) {
      return url;
    }
  }
  
  // Partial match (e.g., "Rio de Janeiro" vs "Rio")
  for (const [key, url] of Object.entries(LOCATION_IMAGES)) {
    const keyNorm = normalizeForMatch(key);
    if (keyNorm.includes(cityNorm) || cityNorm.includes(keyNorm)) {
      return url;
    }
  }
  
  // Fallback to placeholder
  return getPlaceholderImage(city);
}
