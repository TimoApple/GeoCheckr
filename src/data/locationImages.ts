function normalizeForMatch(name: string): string {
  return name.toLowerCase().trim()
    .replace(/ä/g,'ae').replace(/ö/g,'oe').replace(/ü/g,'ue').replace(/ß/g,'ss')
    .replace(/é/g,'e').replace(/è/g,'e').replace(/á/g,'a').replace(/à/g,'a')
    .replace(/ñ/g,'n').replace(/ó/g,'o').replace(/ú/g,'u').replace(/ç/g,'c');
}

export function getCityImage(city: string): string {
  if (LOCATION_IMAGES[city]) return LOCATION_IMAGES[city];
  const cityNorm = normalizeForMatch(city);
  for (const [key, url] of Object.entries(LOCATION_IMAGES)) {
    if (normalizeForMatch(key) === cityNorm) return url;
  }
  for (const [key, url] of Object.entries(LOCATION_IMAGES)) {
    const keyNorm = normalizeForMatch(key);
    if (keyNorm.includes(cityNorm) || cityNorm.includes(keyNorm)) return url;
  }
  const h = Math.abs(city.split('').reduce((a,c)=>((a<<5)-a)+c.charCodeAt(0),0)) % 990 + 10;
  return `https://picsum.photos/id/${h}/800/600`;
}

export function getPlaceholderImage(cityName: string, seed?: number): string {
  const s = seed || Math.abs(cityName.split('').reduce((a,c)=>((a<<5)-a)+c.charCodeAt(0),0)) % 990 + 10;
  return `https://picsum.photos/id/${s}/800/600`;
}

